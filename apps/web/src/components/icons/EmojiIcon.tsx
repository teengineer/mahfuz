// Normalized lookup — strips trailing variation selectors / ZWJ
function lookup(emoji: string): SvgDef | undefined {
  if (MAP[emoji]) return MAP[emoji];
  // Strip VS16 (\uFE0F) and try again
  const stripped = emoji.replace(/\uFE0F/g, "");
  if (MAP[stripped]) return MAP[stripped];
  return undefined;
}

export function EmojiIcon({ emoji, className = "h-5 w-5", fallback = true }: EmojiIconProps) {
  const def = lookup(emoji);

  if (!def) {
    if (fallback) {
      return <span className={className} style={{ display: "inline-flex", alignItems: "center", justifyContent: "center" }}>{emoji}</span>;
    }
    return null;
  }

  const [paths, filled] = def;

  return (
    <svg
      className={className}
      viewBox="0 0 24 24"
      fill={filled ? "currentColor" : "none"}
      stroke={filled ? "none" : "currentColor"}
      strokeWidth={filled ? 0 : 1.5}
      strokeLinecap="round"
      strokeLinejoin="round"
    >
      {paths.split(/(?<=z|Z)\s*(?=M|m)/).map((d, i) => (
        <path key={i} d={d} />
      ))}
    </svg>
  );
}

/** Check if an emoji has an SVG mapping */
export function hasEmojiIcon(emoji: string): boolean {
  return lookup(emoji) !== undefined;
}
